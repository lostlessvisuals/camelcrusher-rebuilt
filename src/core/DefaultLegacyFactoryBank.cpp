std::uint8_t decodeHexNibble(const char value) {
  if (value >= '0' && value <= '9') {
    return static_cast<std::uint8_t>(value - '0');
  }
  if (value >= 'a' && value <= 'f') {
    return static_cast<std::uint8_t>(10 + value - 'a');
  }
  if (value >= 'A' && value <= 'F') {
    return static_cast<std::uint8_t>(10 + value - 'A');
  }
  return 0U;
}

std::vector<std::byte> decodeHexBytes(const std::string_view hex) {
  std::vector<std::byte> bytes;
  bytes.reserve(hex.size() / 2U);
  for (std::size_t index = 0; index + 1U < hex.size(); index += 2U) {
    const auto high = decodeHexNibble(hex[index]);
    const auto low = decodeHexNibble(hex[index + 1U]);
    bytes.push_back(static_cast<std::byte>((high << 4U) | low));
  }
  return bytes;
}

LegacyPresetBank buildDefaultLegacyFactoryBank() {
  const auto bytes = decodeHexBytes(kFactoryChunkHex);
  const auto decoded = decodeLegacyChunk(std::span<const std::byte>(bytes));
  if (!decoded.ok() || !decoded.chunk.has_value()) {
    return {};
  }
  return legacyPresetBankFromChunk(decoded.chunk.value());
}

